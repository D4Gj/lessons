from decimal import Decimal

# int (integer) - целое число 1.txt 2 3 4 5
print(1, 2, 3, 4, 5)
print(type(5))
# float - дробное число 1.txt.0 1.txt.33 0.0003
print(1.333)
print(1.0)
print(type(1.33))
print(Decimal(10.0000000000000000000000000000000005))
print(type(Decimal(10)))
# string – строка '1.txt' 'a' 'asdf' "fsf"
print('100', 100)
print(type('100'))
# boolean - булев тип. Имеет всего два варианта: правда(True) и ложь(False)
print(True)
print(False)
print(type(False))
print(type(True))

